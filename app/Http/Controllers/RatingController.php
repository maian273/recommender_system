    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request, $orderDetailID)
    {

        $rating = new Rating();
        $rating->user_id = Auth::id();
        $rating->point = $request->get('point');
        $orderDetail = OrderDetail::query()->find($orderDetailID)->get();
        $rating->product_id = $orderDetail->id_product;
        $product = Product::query()->find($orderDetail->id_product);
        if ($rating->save()) {
            $product->rating_quatity += 1;
        }
        return redirect($orderDetail->id_product . '/chitiet')->with('message', 'Success');
    }

